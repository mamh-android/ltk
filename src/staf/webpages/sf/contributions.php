<center><a name=top><h1>Making Contributions to STAF</h1></center>
<p>
The STAF project accepts contributions such as software, documentation,
and/or other materials that enhance or extend the STAF project.
Feedback and contributions from the community are the indispensable,
invaluable resource that will ensure the project's continued success.
Please help to improve some aspect of STAF; no improvement is too small
or too simple!</p>
<p>
If you would like to contribute back to the STAF community, please use
the <a href="http://sourceforge.net/tracker/?func=add&group_id=33142&atid=407383">
patch tracker</a> to submit a patch.  When submitting a patch, please
include the following:
<ul>
  <li>The patch file(s) containing your contribution
  <li> The appropriate Statement of Origin <a href="#contrib-stmt">below</a> 
  <li>A description of the functionality you are contributing
  <li>The version of STAF used to create your patch.
  Also, if the patch is relevant to one or more external STAF services,
  provide the version of the service(s) used to create your patch. 
</ul>

Your contribution will be licensed under the
<a href="http://www.opensource.org/licenses/eclipse-1.0.php">EPL</a> (Eclipse
Public License), the license used for the STAF project.  The license has
been approved by the <a href="http://www.opensource.org/">OSI</a> (Open
Source Initiative) as a fully certified open source license.</p>
<hr>
<p>
<a name="contrib-stmt">All</a> contributions, except minor bug fixes,
must include one of the Statements of Origin below.  Insert your name(s)
in the first blank(s) and a high-level summary in the blank in a(i).
Examples of a high-level summary are "Added a new Upgrade service",
"Fixed a bug in the Process service", or "Added a new email STAX extension".</p>
<p>
If your contribution is owned by your employer, someone authorized by
your employer to make such a decision must add a comment to the patch
in the tracker stating that you have permission to contribute it subject
to the license terms of the STAF project.</p>
<p>
<b>Statement of Origin</b>:
