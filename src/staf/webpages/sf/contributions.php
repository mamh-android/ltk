<a href="multi-contrib.txt">Multiple Contributors</a>
</p>
<p>
If you have a question on the contribution policy, send an email
to <a href="mailto:staf-devel@lists.sourceforge.net">
staf-devel@lists.sourceforge.net</a>.</p>

