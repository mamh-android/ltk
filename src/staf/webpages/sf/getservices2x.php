<b>STAF V2 customers -- Time to move ahead!</b> 
<p>
End of service for all STAF V2 releases was April 21, 2006.
As of April 21, 2006, we no longer fix bugs in STAF V2 and do not plan to release
any new versions of STAF V2 (though we will continue to try to answer STAF V2
questions). We recommend that you migrate to STAF V3 at your earliest convenience. 
STAF V3 has significant changes from STAF V2.  If you are currently using STAF V2,
be sure to read the <a href="http://staf.sourceforge.net/current/stafmigrate.html">
STAF V3 Migration Guide</a> to understand changes that you will have to make to migrate
from STAF V2 to STAF V3. 
<p>
The latest releases of services for STAF V2 can be downloaded from
this page.  If you want to download services for STAF V3, go to the
<a href="getservices.php">Download Services for STAF V3"</a> page instead.
Earlier releases of services can be accessed from the
<a href="http://sourceforge.net/project/showfiles.php?group_id=33142">
"See All Releases"</a> page.
