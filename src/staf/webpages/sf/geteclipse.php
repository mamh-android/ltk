<center><a name=top><h1>Download STAF V3 Eclipse Plug-ins</h1></center>
<p>
The latest release of the STAF Eclipse Plug-ins can be downloaded from this page. 
<p>
Recently an effort has begun to provide STAF interaction via the Eclipse Platform.  
This will hopefully provide an entry point for beginning STAF users to get introduced 
to STAF via a GUI environment.  Advanced users may find it useful as it provides access 
to a variety of information displayed in a graphical format and provides a persistent 
history of commands.  Following the spirit of Eclipse, the STAF Editor is 
extensible so that the STAF team and other users may easily add additional pages to 
the STAF Multi-Page Editor. 
<p>
Below is a brief list of features, download link, and a short getting 
started guide.  The Getting Started Guide contains steps to install and begin using
the STAF Eclipse Plug-ins. 
<p>
If you have any comments or suggestions for improving this tool please 
post them to the <a href="http://sourceforge.net/forum/forum.php?forum_id=104045">
STAF Discussion Forum</a>.  This is a Beta release and any input will be 
used to help steer the direction of the tool, so any and all ideas or comments are greatly 
appreciated.
<p>
Features:
<p>
<ul>
<li>Submit any STAF commands via an eclipse editor</li>
<li>Results of commands are saved for later reference</li>
<li>Results of commands displayed in dynamically generated widget based on format of result data</li>
<li>Open editors for multiple machines</li>
<li>Uses the Eclipse extension architecture to allow additional editor tabs to be quickly developed</li>
<li>Currently Implemented Service-Based plug-ins:</li>
<ul>
<li>Service - provides graphical access to the details of currently running services and requests</li>
<li>Trust - provides ability to quickly view and modify trust settings</li>
<li>Var - provides ability to quickly view and modify STAF variables</li>
<li>Handle - provides quick access to STAF handles and allows for easy filtering of which handles are displayed</li>
</ul>
</ul>
<p>
Reference the <a href="eclipsehistory.php">STAF Eclipse Plug-ins History</a> page for version and history
information.
