(STAF-STAX-HandsOn-PartX.odp) and a Microsoft PowerPoint presentation
(STAF-STAX-HandsOn-PartX.ppt) for the lecture portion of the education, and an
HTML file (PartX-Exercises.html) for the exercises.  The presentation files
and exercise files can also be accessed directly via the "Online Education"
section below.
