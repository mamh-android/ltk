<p>
<b>STAF V2 customers -- Time to move ahead!</b> 
<p>
End of service for all STAF V2 releases was April 21, 2006.
As of April 21, 2006, we no longer fix bugs in STAF V2 and no new versions
of STAF V2 will be released. You must migrate to STAF V3 to get support. 
STAF V3 has significant changes from STAF V2.  If you are currently using STAF V2,
be sure to read the <a href="http://staf.sourceforge.net/current/stafmigrate.html">
STAF V3 Migration Guide</a> to understand changes that you will have to make to migrate
from STAF V2 to STAF V3. 
<p>
The latest releases for STAF V2 can be downloaded from this page.
If you want to download the latest releases for STAF V3, go to
the <a href="getcurrent.php">Download STAF V3"</a> page instead.
Earlier releases of STAF can be accessed from the
<a href="http://sourceforge.net/project/showfiles.php?group_id=33142">
"See All Releases"</a> page.

<p>
See the <a href=history2x.php>STAF History</a> page for STAF V2 history
information.
<p>
<h3>Binaries</h3>
<p>
The recommended installation method for STAF is to use InstallShield
MultiPlatform (ISMP).  However, if we haven't yet provided an
InstallShield MultiPlatform file, use the GNU zipped or compressed tar
file, if provided, to do a STAFInst install.
<p>
See the <a href="current2x/STAFUG.htm">STAF V2 User's Guide</a> for more 
detailed instructions for installing STAF V2.
